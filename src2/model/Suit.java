package model;

/***********************************************************************/

class Suit {
	
	private String name;
	
	/***************************************************************************
	*  Method: Constructor - Suit : Initializes the Suit
	*  ****/
	
	Suit(String name) {
		this.name = name;
	}
	
	/* End Method: Constructor - Suit */
	/*
	***************************************************************************
	*  Method: String getName : Returns the Name of the Suit
	*  ****/
	
	String getName() {
		return name;
	}
	
	/* End Method: String getName */

}